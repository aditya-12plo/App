 <template>
    <div class="custom-actions">
      <button class="btn btn-sm" @click="itemAction('take-item', rowData, rowIndex)">TAKE</button>
   </div>

  </template>

  <script>

  export default {
    props: {
      rowData: {
        type: Object,
        required: true
      },
      rowIndex: {
        type: Number
      }
    },
    methods: {
      itemAction (action, data, index) {
      if(action === "take-item")
      {
               this.$root.$emit('takeitem', data , index);
      }
      else
      {
alert('error');
      }
        
      }
      }
      
    
  }
  </script>

